  <P>En Teotihuacan el culto a la Serpiente Emplumada es muy relevante. Aparece en numerosas representaciones en la cerámica, la pintura y la escultura de la ciudad. Uno de los edificios más importantes en el yacimiento arqueológico es el Templo de Quetzalcóatl. Se trata de un edificio compuesto de varios módulos superpuestos de talud-tablero. Los tableros de cada nivel del edificio se encuentran decorados con esculturas de serpientes emplumadas. En el tiempo de apogeo de la ciudad estas piezas estuvieron recubiertas por una rica policromía. Las esculturas de la Serpiente Emplumada alternan con las del dios de la lluvia teotihuacano, que comparte muchos rasgos con las representaciones posclásicas de Tláloc entre los pueblos nahuas.<P>
